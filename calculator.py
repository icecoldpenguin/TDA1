# Accept 2 numbers, num1 and num2
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

# Addition
sum = num1 + num2
print(f"Addition: {sum}")

# Subtraction
diff = num1 - num2
print(f"Subtraction: {diff}")

# Multiplication
mult = num1*num2
print(f"Multiplication: {mult}")

# Division
divi = num1/num2
print(f"Division: {divi}")
